const TronixApp = {
  muteBannerVideo: () => {
    const swiper = window.bannerCarousel;
    const currentVideo = swiper.slides[swiper.activeIndex].children[0];
    const banner_player = videojs(currentVideo.id);
    banner_player.muted(true);
  },

  unmuteBannerVideo: () => {
    const swiper = window.bannerCarousel;
    const currentVideo = swiper.slides[swiper.activeIndex].children[0];
    const banner_player = videojs(currentVideo.id);
    banner_player.muted(false);
  },

  startModalVideo: () => {
    const modalVideo = document.querySelector(".modal video");
    modalVideo.play();
  },

  pauseModalVideo: () => {
    const modalVideo = document.querySelector(".modal video");
    modalVideo.currentTime = 0;
    modalVideo.pause();
  },

  modal: {
    toggleMuteVideo: (mutedIcon, unmutedIcon) => {
      const modalVideo = document.querySelector(".modal video");
      if (modalVideo.muted) {
        modalVideo.muted = false;
        mutedIcon.setAttribute("style", "display: none");
        unmutedIcon.setAttribute("style", "display: block");
      } else {
        modalVideo.muted = true;
        mutedIcon.setAttribute("style", "display: block");
        unmutedIcon.setAttribute("style", "display: none");
      }
    },
  },
};

